     public void actualizarDatos(){
         conectar cc = new conectar();
        Connection cn = cc.con;
        String id, nom, apell, fech, med, camp;
        String sql ="";
   
        nom = txt_nom.getText();
        apell = txt_apell.getText();
        fech = txt_fech.getText();
        med = txt_med.getText();
        
        sql ="update entrenador set nombre=?, apellido=?, fecha_nacimiento=?, numero_medallas=?, campeon_actual=? where id=?";
      
        int filaSelec=tabla.getSelectedRow();       
        String dao=(String)tabla.getValueAt(filaSelec, 0);
        
        try {
            PreparedStatement pst = cn.prepareStatement(sql);
          
            pst.setString(1, nom);
            pst.setString(2, apell);
            pst.setString(3, fech);
            pst.setString(4, med);
            pst.setString(5, campeon);
            pst.setString(6, dao);
            
            int n=pst.executeUpdate();
            if(n>0){
                JOptionPane.showMessageDialog(null, "Resgistro editado con exito");
                noVisibles();
            }
        } catch (SQLException ex) {
            Logger.getLogger(entrenador.class.getName()).log(Level.SEVERE, null, ex);
        }      
    }
  
