    public void mostrarDatos(){
               

        String[] titulos={"ID", "Nombre", "Apellido", "Fecha Nacimiento", "Numero medallas", "Campeon Actual"};
        String[] registros=new String[7];
        
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        
        String sql="select * from entrenador";
        
        try {
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(sql);
            
          while(rs.next()){
              
              registros[0]=rs.getString("id");
              registros[1]=rs.getString("nombre");
              registros[2]=rs.getString("apellido");
              registros[3]=rs.getString("fecha_nacimiento");
              registros[4]=rs.getString("numero_medallas");
              registros[5]=rs.getString("campeon_actual");
              
              modelo.addRow(registros);
              
          }
          tabla.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Eror al mostrar datos" +e.getMessage());
        }
    }
